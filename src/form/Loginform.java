    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText().trim();
        String password = String.valueOf(jPasswordField1.getPassword()).trim();

        
        User user = userDAO.login(username, password);
        if (user != null) {
            JOptionPane.showMessageDialog(this, "Login berhasil: " + user.getUsername());
            // Bisa buka frame lain di sini
            String namaUser = user.getNama();
            Dasboard db = new Dasboard(namaUser);
            db.setVisible(true);

        // Tutup form login
        this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Username atau password salah!");
        }
   
    }//GEN-LAST:event_jButton1ActionPerformed

